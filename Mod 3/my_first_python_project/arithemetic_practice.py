# two input for operation
a = int(input("Enter A :"))
b = int(input("Enter B :"))

# addition

sum = a + b

print("summation :", sum)

# subtraction

sub = a - b

print("subtraction :", sub)

# division

division = a / b

print("Division :", division)

#Multiplication

mul = a * b

print("Multiplication :", mul)

# Modulus

mod = a % b
print("Modulus :", mod)

# Exponential
exp = a ** b

print("Exponential :", exp)

# divisor
divisor = a // b

print("divisor :", divisor)